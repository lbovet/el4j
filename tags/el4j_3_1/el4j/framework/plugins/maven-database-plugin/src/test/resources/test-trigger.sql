/* Create Triggers */
CREATE SEQUENCE ABC
INCREMENT BY 1
START WITH 1
NOMAXVALUE
MINVALUE 1
NOCYCLE
NOCACHE
NOORDER;

CREATE OR REPLACE TRIGGER SET_ABC_KEYID
BEFORE INSERT
ON BP_AGENT
FOR EACH ROW
BEGIN
  SELECT ABC.NEXTVAL
  INTO :NEW.KEYID
  FROM DUAL;
END;
/

