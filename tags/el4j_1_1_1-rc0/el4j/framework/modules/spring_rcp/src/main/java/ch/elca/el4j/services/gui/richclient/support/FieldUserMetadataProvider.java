package ch.elca.el4j.services.gui.richclient.support;

import java.util.Map;

/**
 * A provider for user metadata.
 *
 * <script type="text/javascript">printFileStatus
 *   ("$URL$",
 *    "$Revision$",
 *    "$Date$",
 *    "$Author$"
 * );</script>
 *
 * @see org.springframework.binding.form.FieldMetadata
 * @author Adrian Moos (AMS)
 */
public interface FieldUserMetadataProvider {
    /**
     * Returns the user metadata for the field named {@code fieldName}.
     * The returned map need not contain an entry for every key. Also, null 
     * values are permitted, as is returning null.
     */ 
    Map<String, Object> getAll(String fieldName);
}
