-- Oracle key ranges: 1 - 2147483647 remote, 2147483648 - 4294967295 local
-- (this is 1 <-> 2^31-1 and 2^31 <-> 2^32-1)

CREATE SEQUENCE simpleperson_sequence INCREMENT BY 1 START WITH 1 MINVALUE 1 MAXVALUE 2147483647;
CREATE SEQUENCE person_sequence INCREMENT BY 1 START WITH 1 MINVALUE 1 MAXVALUE 2147483647;

CREATE TABLE SIMPLEPERSON (
    ID BIGINT NOT NULL PRIMARY KEY,
    VERSION BIGINT NOT NULL,
    NAME VARCHAR(40) NOT NULL,
    EMAIL VARCHAR(40) NOT NULL
);

CREATE TABLE PERSON (
	ID BIGINT NOT NULL PRIMARY KEY,
    VERSION BIGINT NOT NULL,
	NAME VARCHAR(40) NOT NULL,
	PARENT BIGINT REFERENCES PERSON(ID)
);
