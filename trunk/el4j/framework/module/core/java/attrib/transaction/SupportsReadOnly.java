package attrib.transaction;

import org.springframework.transaction.interceptor.DefaultTransactionAttribute;

/**
 * This class specifies a supports read only transaction attribute.
 * It must be used standalone.
 * 
 * <script type="text/javascript">printFileStatus
 *   ("$Source$",
 *    "$Revision$",
 *    "$Date$",
 *    "$Author$"
 * );</script>
 *
 * @author Martin Zeltner (MZE)
 */
public class SupportsReadOnly extends DefaultTransactionAttribute {

    /**
     * Default constructor.
     */
    public SupportsReadOnly() {
        super(PROPAGATION_SUPPORTS);
        setReadOnly(true);
    }
}