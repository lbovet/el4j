package ch.elca.el4j.services.persistence.hibernate.validation;

import static java.lang.annotation.ElementType.TYPE;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

import java.lang.annotation.Documented;
import java.lang.annotation.Retention;
import java.lang.annotation.Target;

import org.hibernate.validator.ValidatorClass;

/**
 * 
 * This annotation enables validation of the annotated bean using a validation
 * method defined by the bean.
 *
 * <script type="text/javascript">printFileStatus
 *   ("$URL$",
 *    "$Revision$",
 *    "$Date$",
 *    "$Author$"
 * );</script>
 *
 * @author Alex Mathey (AMA)
 */

@Documented
@ValidatorClass(ValidateValidator.class)
@Target(TYPE)
@Retention(RUNTIME)
public @interface Validate {
   
    /**
     * The name of the validation method defined by the annotated bean. 
     */
    String validationMethod() default "validate";
    
    /**
     * Error message.
     */
    String message() default "must be the name of a method defined by the "
        + "annotated bean";
}
